"""Дано некоторое число:
12345
Выведите в консоль все его символы с конца.
Метод с созданием переменной
"""

def reverse_digits(n):
    """Возвращает цифры числа в обратном порядке"""
    return str(abs(n))[::-1]

print(reverse_digits(12345))   # '54321'
print(reverse_digits(-12345))  # '54321' (игнорирует минус)
print(reverse_digits(0))       # '0'