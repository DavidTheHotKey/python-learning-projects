"""
Даны два целых числа. Проверьте, что первое число без остатка делится на второе.
"""
first_int = input("Введите число 1 :\n")
second_int = input("Введите число 2 :\n")

try:
    # Преобразуем ввод в целые числа
    num1 = int(first_int)
    num2 = int(second_int)

    # Проверяем деление на 0
    if num2 == 0:
        print("Ошибка: деление на ноль!")
    else:
        # Проверяем делимость без остатка
        if num1 % num2 == 0:
            print("Делится нацело")
        else:
            print("Не делится нацело")

except ValueError:
    print('Введите целое число')